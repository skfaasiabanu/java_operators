public class StringToInteger
{
    public static void main(String args[])
    {
        String a="10";
        String b="15";
        int a1=Integer.parseInt(a);
        int b1=Integer.parseInt(b);
        System.out.println(a1+b1);
        System.out.println(a1-b1);
        System.out.println(a1*b1);
        System.out.println(a1/b1);
        System.out.println(a1%b1);
    }
}